void TileMap::draw(const RenderWorld *world)
{
    glm::vec2 originalPosition = m_sprite.position();

    for (int j = 0; j < m_map.size(); ++j) { 
        for (int i = 0; i < m_map[j].size(); ++i) {
            m_sprite.setX(originalPosition.x + i * m_sprite.subRect().w);
            m_sprite.setY(originalPosition.y + j * m_sprite.subRect().h);
            world->draw(&m_sprite);
            m_sprite.setPosition(originalPosition);
        }
    }
}
